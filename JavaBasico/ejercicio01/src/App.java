public class App {
    public static void main(String[] args) throws Exception {

        //Byte
        byte bt = 100;
        System.out.println("Soy un Byte: "+bt);

        //Short
        short sh = 150;
        System.out.println("Soy un Short: "+sh);

        //Integer
        Integer it = 10;
        System.out.println("Soty un Integer: "+it);

        //Long 
        long lg = -9223372036854775808L;
        System.out.println("Soy un Long: "+lg);

        //Float
        float ft = 1.0f;
        System.out.println("Soy un Float: "+ft);

        //Double
        double db = 1.0;
        System.out.println("Soy un Double: "+db);

        //Boolean
        boolean b = true;
        System.out.println("Soy un Boolean: "+b);

        //Character
        char ch = 'a';
        System.out.println("Soy un Character: "+ch);

        //String
        String st = "Soy un String";
        System.out.println(st);
    }
}
